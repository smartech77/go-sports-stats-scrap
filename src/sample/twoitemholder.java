package sample;

public class twoitemholder {

    int start = -10;
    int end =   -10;

    public twoitemholder() {
    }

    public twoitemholder(int start, int end) {
        this.start = start;
        this.end = end;
    }


    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }


}
